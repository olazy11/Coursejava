package Class6;

public class SwitchDemo4 {
    public static void main(String[] args) {


        String day = "Friday";
        switch (day) {
            case "Monday":
                System.out.println("M");
                break;
            case "Tuesday":
                System.out.println("T");
                break;
            case "Wednesday":
                System.out.println("W");
                break;
            default:
                System.out.println("wrong day");

        }

    }
}

