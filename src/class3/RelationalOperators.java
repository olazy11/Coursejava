package class3;

public class RelationalOperators {
    public static void main(String[] args) {

        /*
        >
        =>
        ==
        !=
        <
        =<

         */
        int number =10;
        int number2=10;
        System.out.println(number+number2);

        boolean result =20>10;
        System.out.println(result);

        System.out.println(20>10);//is 20 greater than 10=>true
        System.out.println(25>10);//is 5 greater than 10=>true
        System.out.println(5!=10);//is 5 is not equal to 10=true
        System.out.println(5==10);//is 5 equal to 10=false

    }
}
